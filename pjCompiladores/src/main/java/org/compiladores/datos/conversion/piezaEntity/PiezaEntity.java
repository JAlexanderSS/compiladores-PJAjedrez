package org.compiladores.datos.conversion.piezaEntity;



public class PiezaEntity {
    private String tipoPieza;
    private String piezaFinal;
    private String piezaInicio;

    public String getTipoPieza() {
        return tipoPieza;
    }

    public void setTipoPieza(String tipoPieza) {
        this.tipoPieza = tipoPieza;
    }

    public String getPiezaFinal() {
        return piezaFinal;
    }

    public void setPiezaFinal(String piezaFinal) {
        this.piezaFinal = piezaFinal;
    }

    public String getPiezaInicio() {
        return piezaInicio;
    }

    public void setPiezaInicio(String piezaInicio) {
        this.piezaInicio = piezaInicio;
    }
}
